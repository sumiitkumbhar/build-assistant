async function generateAnswer(
  query: string,
  chunks: EnhancedChunk[],
  webCitations: TransformedCitation[],
  feasibilitySection = "",
  drawingValidation?: any
): Promise<{
  answerMarkdown: string;
  missingCoverage: Array<{
    questionPart: string;
    resolvedFrom: "document" | "web" | "unresolved";
  }>;
}> {
  const chosenChunks = chooseChunksForAnswer(query, chunks);
  const strictDocumentContext = buildStrictDocumentContext(chosenChunks);
  const webContext = buildWebContext(webCitations);

  const drawingSection = drawingValidation
    ? `

DRAWING VALIDATION RESULTS (structured data – do not change numbers, just explain logically):
${JSON.stringify(drawingValidation, null, 2)}`
    : "";

  const systemPrompt = `${systemMessage}

You must follow these rules strictly:

1. Answer primarily from DOCUMENT context.
2. Use inline citations like [D1], [D2] for document-backed claims.
3. Use [W1], [W2] only for claims supported by web fallback.
4. Never invent page numbers, clause numbers, or regulations.
5. If the answer is not supported, say so clearly.
6. Keep the answer structured in markdown.
7. Do not output JSON unless explicitly asked.
8. Do not mention sources you did not use.
9. If document evidence is strong, do not rely on web.
10. If feasibility or drawing data is provided, explain it without changing numeric values.`;

  const userPrompt = `
User query:
${query}

DOCUMENT CONTEXT:
${strictDocumentContext || "No document context available."}

WEB CONTEXT:
${webContext || "No web context available."}
${feasibilitySection || ""}${drawingSection}

Return JSON only in this exact shape:
{
  "answerMarkdown": "markdown answer with inline citations like [D1], [D2], [W1]",
  "missingCoverage": [
    {
      "questionPart": "string",
      "resolvedFrom": "document" | "web" | "unresolved"
    }
  ]
}
`.trim();

  try {
    const completion = await groq.chat.completions.create({
      model: GROQ_CHAT_MODEL,
      messages: [
        { role: "system", content: systemPrompt },
        { role: "user", content: userPrompt },
      ],
      temperature: 0.2,
      max_tokens: 2200,
      top_p: 1,
    });

    const raw = completion.choices?.[0]?.message?.content?.trim() || "";
    const jsonText = extractFirstJsonObject(raw);

    if (!jsonText) {
      return {
        answerMarkdown: buildPartialCoverageAnswer(),
        missingCoverage: [
          {
            questionPart: query,
            resolvedFrom: "unresolved",
          },
        ],
      };
    }

    const parsed = JSON.parse(jsonText);

    let answerMarkdown =
      typeof parsed?.answerMarkdown === "string"
        ? parsed.answerMarkdown.trim()
        : "";

    let missingCoverage: Array<{
      questionPart: string;
      resolvedFrom: "document" | "web" | "unresolved";
    }> = Array.isArray(parsed?.missingCoverage)
      ? parsed.missingCoverage.map((m: any) => ({
          questionPart: String(m?.questionPart || query),
          resolvedFrom:
            m?.resolvedFrom === "document" ||
            m?.resolvedFrom === "web" ||
            m?.resolvedFrom === "unresolved"
              ? m.resolvedFrom
              : "unresolved",
        }))
      : [];

    if (!answerMarkdown) {
      answerMarkdown = buildPartialCoverageAnswer();
    }

    if (shouldRejectWeakExtraction(query, answerMarkdown)) {
      const extractionPrompt = buildExtractionPrompt(
        query,
        strictDocumentContext
      );

      const retryCompletion = await groq.chat.completions.create({
        model: GROQ_CHAT_MODEL,
        messages: [
          {
            role: "system",
            content:
              "You are a precise regulatory extraction engine. Output valid JSON only.",
          },
          {
            role: "user",
            content: extractionPrompt,
          },
        ],
        temperature: 0.1,
        max_tokens: 2200,
        top_p: 1,
      });

      const retryRaw =
        retryCompletion.choices?.[0]?.message?.content?.trim() || "";
      const retryJson = extractFirstJsonObject(retryRaw);

      if (retryJson) {
        const retryParsed = JSON.parse(retryJson);

        const retryAnswer =
          typeof retryParsed?.answerMarkdown === "string"
            ? retryParsed.answerMarkdown.trim()
            : "";

        const retryMissingCoverage: Array<{
          questionPart: string;
          resolvedFrom: "document" | "web" | "unresolved";
        }> = Array.isArray(retryParsed?.missingCoverage)
          ? retryParsed.missingCoverage.map((m: any) => ({
              questionPart: String(m?.questionPart || query),
              resolvedFrom:
                m?.resolvedFrom === "document" ||
                m?.resolvedFrom === "web" ||
                m?.resolvedFrom === "unresolved"
                  ? m.resolvedFrom
                  : "unresolved",
            }))
          : [];

        if (retryAnswer) {
          answerMarkdown = retryAnswer;
          missingCoverage = retryMissingCoverage;
        }
      }
    }

    if (!missingCoverage.length) {
      missingCoverage = [
        {
          questionPart: query,
          resolvedFrom: chunks.length
            ? "document"
            : webCitations.length
            ? "web"
            : "unresolved",
        },
      ];
    }

    return {
      answerMarkdown,
      missingCoverage,
    };
  } catch (error) {
    console.error("generateAnswer failed:", error);

    return {
      answerMarkdown: buildPartialCoverageAnswer(),
      missingCoverage: [
        {
          questionPart: query,
          resolvedFrom: "unresolved",
        },
      ],
    };
  }
}


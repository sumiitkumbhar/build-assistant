function dominantJurisdiction(
  chunks: EnhancedChunk[]
): "uk" | "india" | "usa" | null {
  if (!chunks.length) return null;

  const counts: Record<string, number> = {};
  for (const c of chunks) {
    const key = lower(c.region || c.jurisdiction);
    if (!key) continue;
    counts[key] = (counts[key] || 0) + 1;
  }

  const top =
    Object.entries(counts).sort((a, b) => b[1] - a[1])[0]?.[0] || null;
  if (top === "uk" || top === "india" || top === "usa") return top;
  return null;
}

function hasJurisdictionMismatch(
  query: string,
  chunks: EnhancedChunk[]
): boolean {
  if (isComparisonQuery(query)) return false;

  const requested = detectRequestedJurisdiction(query);
  const dominant = dominantJurisdiction(chunks);

  if (!requested || !dominant) return false;
  return requested !== dominant;
}


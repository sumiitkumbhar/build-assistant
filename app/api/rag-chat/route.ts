function chooseChunksForAnswer(
  query: string,
  chunks: EnhancedChunk[]
): EnhancedChunk[] {
  const top = chunks.slice(0, 8);

  if (isRequirementDefinitionQuery(query)) {
    const defs = top.filter((c) => {
      const heading = lower(c.section_heading);
      const section = lower(c.section_hierarchy);
      const content = lower(c.content);
      return (
        heading.includes("requirement") ||
        section.includes("requirement") ||
        content.includes("the building shall be designed and constructed")
      );
    });

    if (defs.length) return defs.slice(0, 4);
  }

  if (isTableQuery(query)) {
    return chunks
      .filter((c) => {
        const content = lower(c.content);
        return (
          content.includes("table 2.1") ||
          content.includes("travel distance") ||
          /\b\d+\s?m\b/.test(content)
        );
      })
      .slice(0, 6);
  }

  if (isThresholdQuery(query)) {
    const thresholdChunks = top
      .map((c) => ({
        chunk: c,
        priority: getFirefightingShaftClausePriority(c),
      }))
      .filter(({ priority, chunk }) => {
        const text = lower(chunk.content);
        return (
          priority > 0 ||
          text.includes("required") ||
          text.includes("must be provided") ||
          text.includes("regulation 38")
        );
      })
      .sort(
        (a, b) => b.priority - a.priority || a.chunk.distance - b.chunk.distance
      )
      .map(({ chunk }) => chunk);

    if (thresholdChunks.length) {
      return thresholdChunks.slice(0, 6);
    }
  }

  return chunks.slice(0, 10);
}


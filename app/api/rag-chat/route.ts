const GEMINI_EMBEDDING_MODEL =
  process.env.GEMINI_EMBEDDING_MODEL || "gemini-embedding-001";

async function generateEmbedding(text: string): Promise<number[]> {
  const cleanText = text.trim();
  if (!cleanText) {
    throw new Error("Cannot generate embedding for empty text");
  }

  const response = await ai.models.embedContent({
    model: GEMINI_EMBEDDING_MODEL,
    contents: cleanText,
  });

  const values = response.embeddings?.[0]?.values;
  if (!values?.length) {
    throw new Error("Embedding API returned no values");
  }

  return Array.from(values);
}

function escapeRegExp(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function optimizeQuery(rawQuery: string): string {
  const fillers = [
    "according to",
    "how is it",
    "include the",
    "what is",
    "can you",
    "please",
    "tell me",
    "explain",
  ];
  let cleaned = rawQuery.toLowerCase();
  for (const f of fillers) {
    cleaned = cleaned.replace(new RegExp(escapeRegExp(f), "gi"), "");
  }
  const expansions: Record<string, string> = {
    fsi: "FSI Floor Space Index Floor Area Ratio FAR",
    fsr: "FSR Floor Space Ratio FAR FSI",
    far: "FAR Floor Area Ratio FSI FSR",
    dcr: "DCR Development Control Regulations",
    rera: "RERA Real Estate Regulation",
  };
  for (const key of Object.keys(expansions)) {
    if (cleaned.includes(key)) cleaned += " " + expansions[key];
  }
  return cleaned.trim();
}


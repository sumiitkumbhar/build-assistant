function normalizeSentenceSpacing(text: string) {
  if (!text) return "";

  return text
    .replace(/([.?!;:])([A-Z])/g, "$1 $2")
    .replace(/([a-z])(\d+\.\d+)/g, "$1 $2")
    .replace(/(\d+\.\d+)([A-Z])/g, "$1 $2")
    .replace(/([a-z])([A-Z][a-z])/g, "$1 $2")
    .replace(/([a-z])([A-Z]{2,})/g, "$1 $2")
    .replace(/([a-z])\(/g, "$1 (")
    .replace(/\)\(/g, ") (")
    .replace(/\s{2,}/g, " ")
    .trim();
}

function sanitizeEvidenceText(text: string) {
  return normalizeSentenceSpacing(
    repairSmashedWords(stripChunkMetadata(text || ""))
  );
}

function normalizedForMatching(text: string): string {
  return lower(sanitizeEvidenceText(text || ""))
    .replace(/\s+/g, " ")
    .trim();
}

function looksIncompleteEvidence(text: string) {
  if (!text) return false;

  const t = text.trim();
  if (!t) return false;

  const lastLine =
    t
      .split("\n")
      .map((line) => line.trim())
      .filter(Boolean)
      .pop() || t;

  return (
    /\b(and|or|to|of|for|with|including|which|that|where|when|if|than|see|paragraph|paragraphs)$/i.test(
      lastLine
    ) ||
    /\($/.test(lastLine) ||
    /[:;,-]$/.test(lastLine) ||
    /^(\d+\.|[a-z]\.)$/i.test(lastLine)
  );
}

function buildExcerpt(text: string, max = 240) {
  if (!text) return "";

  const cleaned = sanitizeEvidenceText(text);
  if (!cleaned) return "";

  const sentenceMatch = cleaned.match(/.*?[.!?](\s|$)/);
  const firstSentence = sentenceMatch?.[0]?.trim();

  if (
    firstSentence &&
    firstSentence.length >= 50 &&
    firstSentence.length <= max
  ) {
    return firstSentence;
  }

  return cleaned.length > max ? `${cleaned.slice(0, max).trim()}…` : cleaned;
}

function stitchAdjacentChunks(
  chunks: EnhancedChunk[],
  startIndex: number,
  maxChars = 2400
) {
  const base = chunks[startIndex];
  if (!base) return "";

  let assembled = sanitizeEvidenceText(base.content);
  if (!looksIncompleteEvidence(assembled)) return assembled;

  const sameDocFollowers = chunks
    .slice(startIndex + 1)
    .filter((c) => c.doc_title === base.doc_title)
    .sort((a, b) => {
      const aPage = a.page_from ?? 999999;
      const bPage = b.page_from ?? 999999;
      if (aPage !== bPage) return aPage - bPage;

      const aLine = a.line_from ?? 999999;
      const bLine = b.line_from ?? 999999;
      return aLine - bLine;
    });

  for (const next of sameDocFollowers) {
    const nextText = sanitizeEvidenceText(next.content);
    if (!nextText) continue;

    const nextPage = next.page_from ?? base.page_from ?? null;
    const basePage = base.page_from ?? null;
    if (basePage != null && nextPage != null && nextPage - basePage > 2) {
      break;
    }

    assembled = `${assembled} ${nextText}`.replace(/\s{2,}/g, " ").trim();

    if (!looksIncompleteEvidence(assembled) || assembled.length >= maxChars) {
      break;
    }
  }

  return assembled.slice(0, maxChars).trim();
}

function transformChunksForFrontend(
  chunks: EnhancedChunk[]
): TransformedCitation[] {
  return chunks.map((chunk, index) => {
    const stitchedFullText = stitchAdjacentChunks(chunks, index);
    const cleanedFullText = sanitizeEvidenceText(
      stitchedFullText || chunk.content
    );

    return {
      id: `D${index + 1}`,
      title: chunk.doc_title,
      type: mapDocKindToCitationKind(chunk.doc_kind),
      sourceType: "document",
      pageNumber: chunk.page_from ?? undefined,
      clauseNumber:
        chunk.citation_value && String(chunk.citation_value).trim()
          ? String(chunk.citation_value).trim()
          : undefined,
      section: chunk.section_heading ?? chunk.section_hierarchy ?? undefined,
      fullText: cleanedFullText,
      excerpt: buildExcerpt(cleanedFullText, 220),
      confidence: Math.round(
        Math.max(0, Math.min(100, (1 - chunk.distance) * 100))
      ),
      lastUpdated: chunk.indexed_at,
      directLink: chunk.doc_path?.startsWith("http")
        ? chunk.doc_path
        : undefined,
      sourceLabel: buildDocSourceLabel(chunk, index),
      _raw: {
        ...chunk,
        originalContent: chunk.content,
        backendSanitized: true,
        stillLooksIncomplete: looksIncompleteEvidence(cleanedFullText),
      },
    };
  });
}

function dedupeCitations(
  citations: TransformedCitation[]
): TransformedCitation[] {
  const seen = new Set<string>();

  return citations.filter((c) => {
    const normalizedExcerpt = (c.excerpt || "")
      .toLowerCase()
      .replace(/\s+/g, " ")
      .replace(/diagram\s+\d+(\.\d+)?/g, "")
      .trim()
      .slice(0, 90);

    const key = [
      c.title || "",
      c.pageNumber || "",
      c.clauseNumber || "",
      normalizedExcerpt,
    ].join("|");

    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function calculateConfidence(avgDistance: number, chunkCount: number): number {
  let conf = Math.max(0, 1 - avgDistance);
  if (avgDistance < 0.3) conf = Math.min(1, conf * 1.15);
  if (chunkCount < 3) conf *= 0.7;
  if (chunkCount >= 5) conf *= 1.05;
  return Math.min(1, conf);
}

function calculateMetadata(chunks: EnhancedChunk[]) {
  const docs = new Set<string>();
  const citationTypes: Record<string, number> = {};
  const authorities: Record<string, number> = {};
  const topics: Record<string, number> = {};
  let totalDistance = 0;

  for (const c of chunks) {
    totalDistance += c.distance;
    docs.add(c.doc_title);
    if (c.citation_type) {
      citationTypes[c.citation_type] =
        (citationTypes[c.citation_type] || 0) + 1;
    }
    if (c.doc_authority) {
      authorities[c.doc_authority] = (authorities[c.doc_authority] || 0) + 1;
    }
    if (c.topic) topics[c.topic] = (topics[c.topic] || 0) + 1;
  }

  return {
    avgDistance: chunks.length ? totalDistance / chunks.length : 0,
    documentsFound: docs.size,
    citationTypes,
    authorities,
    topics,
  };
}


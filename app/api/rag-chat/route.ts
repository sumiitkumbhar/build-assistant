function buildReferencesBlock(
  documentCitations: TransformedCitation[],
  webCitations: TransformedCitation[]
): string {
  const docRefs = documentCitations.map(
    (c) => `- ${formatVerifiedReference(c)}`
  );

  const webRefs = webCitations.map((c) => {
    const bits = [
      `[${c.id}]`,
      c.title,
      "taken from web",
      `Confidence ${c.confidence}%`,
    ];
    return `- ${bits.join(" — ")}`;
  });

  let out = `\n\n### References`;

  if (docRefs.length > 0) {
    out += `\n\n**Document References**\n${docRefs.join("\n")}`;
  }

  if (webRefs.length > 0) {
    out += `\n\n**Web References**\n${webRefs.join("\n")}`;
  }

  return out;
}


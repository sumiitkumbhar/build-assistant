function getFirefightingShaftClausePriority(chunk: EnhancedChunk): number {
  const text = normalizedForMatching(chunk.content || "");
  const clause = lower(chunk.clause_label);
  const citation = lower(chunk.citation_value);
  const heading = normalizedForMatching(chunk.section_heading || "");
  const section = normalizedForMatching(chunk.section_hierarchy || "");

  let score = 0;

  const isMain17_2 =
    clause === "17.2" ||
    citation === "17.2" ||
    text.includes("17.2") ||
    text.includes(
      "storey more than 18 m above the fire and rescue service vehicle access level"
    );

  const isBasement17_3 =
    clause === "17.3" ||
    citation === "17.3" ||
    text.includes("17.3") ||
    text.includes("basement more than 10 m below") ||
    text.includes("two or more basement storeys");

  const isService17_4 =
    clause === "17.4" ||
    citation === "17.4" ||
    text.includes("17.4") ||
    text.includes("should serve all storeys through which they pass");

  const isMulti17_5 =
    clause === "17.5" ||
    citation === "17.5" ||
    text.includes("17.5") ||
    text.includes("minimum of two firefighting shafts");

  const isSpecial17_6 =
    clause === "17.6" ||
    citation === "17.6" ||
    text.includes("17.6") ||
    text.includes("at least two firefighting shafts");

  const isDiagramOnly =
    !text.includes("should have") &&
    !text.includes("required") &&
    !text.includes("must be provided") &&
    (heading.includes("diagram") ||
      section.includes("diagram") ||
      text.startsWith("diagram 17.1") ||
      text.startsWith("diagram 17.2") ||
      text.includes("components of a firefighting shaft") ||
      text.includes("location of firefighting shafts"));

  if (isMain17_2) score += 100;
  if (isBasement17_3) score += 60;
  if (isService17_4) score += 35;
  if (isMulti17_5) score += 30;
  if (isSpecial17_6) score += 25;
  if (text.includes("18 m")) score += 10;
  if (text.includes("firefighting shaft")) score += 8;
  if (isDiagramOnly) score -= 80;

  return score;
}

